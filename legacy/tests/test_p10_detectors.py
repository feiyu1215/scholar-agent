"""
Unit tests for P10 programmatic detectors:
- THROAT_CLEARING
- PROMOTIONAL_LANGUAGE
- INFLATED_SYMBOLISM
- PASSIVE_VOICE_OVERUSE
- PARALLEL_STRUCTURE
"""
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

import json
from tools.deai_engine import _detect_programmatic_signals


def _signal_types(text: str) -> set:
    """Helper: return set of signal_type names detected."""
    return {s.signal_type for s in _detect_programmatic_signals(text)}


# ─── THROAT_CLEARING ──────────────────────────────────────────────────────────

def test_throat_clearing_fires_on_two_phrases():
    text = (
        "It is important to note that the model converges slowly. "
        "The learning rate was set to 0.001. "
        "It bears mentioning that the batch size affects stability. "
        "We trained for 100 epochs with early stopping."
    )
    assert "THROAT_CLEARING" in _signal_types(text)


def test_throat_clearing_silent_on_single_phrase():
    text = (
        "It is worth noting that the results are preliminary. "
        "The main finding is a 15% improvement in F1 score. "
        "We validated on three separate test sets."
    )
    assert "THROAT_CLEARING" not in _signal_types(text)


def test_throat_clearing_fires_on_gold_012():
    case = json.loads(Path("eval/gold_deai/deai_gold_012.json").read_text())
    assert "THROAT_CLEARING" in _signal_types(case["ai_text"])


