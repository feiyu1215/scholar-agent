# ─── Cross-cutting: no false positives on clean text ──────────────────────────

def test_clean_academic_text_minimal_signals():
    """A well-written academic paragraph should trigger very few signals."""
    text = (
        "We examine the effect of minimum wage increases on employment "
        "in the restaurant industry. Using a difference-in-differences design "
        "that exploits state-level policy variation, we find that a 10% increase "
        "in the minimum wage reduces employment by 1.2 percentage points. "
        "This estimate is robust to alternative specifications and sample restrictions. "
        "Our findings contribute to the ongoing debate about the employment effects "
        "of minimum wage legislation."
    )
    signals = _signal_types(text)
    # Should NOT trigger any of the P10 signals
    p10_signals = {
        "THROAT_CLEARING", "PROMOTIONAL_LANGUAGE", "INFLATED_SYMBOLISM",
        "PASSIVE_VOICE_OVERUSE", "PARALLEL_STRUCTURE",
    }
    false_positives = signals & p10_signals
    assert len(false_positives) == 0, f"False positives: {false_positives}"


